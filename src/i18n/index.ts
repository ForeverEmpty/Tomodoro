export const i18n = createI18n({
  fallbackLocale: 'en-US',
  legacy: false,
  locale: 'zh-CN',
  messages,
})

export const useI18n = () => {
  const settingsStore = useSettingsStore()
  const { setting } = storeToRefs(settingsStore)
  const language = computed(() => setting.value.language)
  const { locale, t: translate } = useVueI18n({ useScope: 'global' })

  watch(
    language,
    (nextLanguage) => {
      locale.value = nextLanguage
      if (typeof document !== 'undefined') {
        document.documentElement.lang = nextLanguage
      }
    },
    { immediate: true },
  )

  return {
    language,
    t: (key: TranslationKey, params?: TranslationParams) => translate(key, params ?? {}),
  }
}
